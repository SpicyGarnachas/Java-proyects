package diegomartinez.tiposdedatos;

public class TipoDatos {
    public static void main(String[] args){
        byte num1 = 127;
        short num2 = 32767;
        int num3 = 3867000;
        long num4 = 372502;
        float num5 = 9.81f;
        double num6 = 77.989;
        boolean cond = false;
        String nombre = "JUAN";
        char car ='J';
        System.out.println("Número tipo BYTE: " + num1);
        System.out.println("Número tipo SHORT: " + num2);
        System.out.println("Número tipo INT: " + num3);
        System.out.println("Número tipo LONG:" + num4);
        System.out.println();
        System.out.println("Número tipo Decimal: " + num5);
        System.out.println("Número tipo DOBLE : " + num6);
        System.out.println("CONDICIONAL: " + cond);
        System.out.println("CADENA:" + nombre);
        System.out.println("CARACTER: " + car);
    }
}
